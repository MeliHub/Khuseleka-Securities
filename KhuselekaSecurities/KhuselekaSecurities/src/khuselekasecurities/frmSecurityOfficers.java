    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      
        setUptextFields();
        getDataAndDisplay();
        String strDBConnectionString = "jdbc:mysql://localhost:3306/gate_control";
        String strDBUser="root";
        String strDBPassword="NewPassword";
        java.sql.Connection conMySQLConnectionString;
        Statement stStatement=null;

        try 
        {
           
            conMySQLConnectionString = DriverManager.getConnection(strDBConnectionString,strDBUser,strDBPassword);
        stStatement = conMySQLConnectionString.createStatement();
        
        String strQuery = "INSERT INTO security_guards (Name,Last_Name,Shift,Contact_Number,App_Password)" + " VALUES ('"+name+"','"+lastname+"','"+shift+"','"+contactnumber+"','"+apppassword+"');";
       
        
        stStatement.executeUpdate(strQuery);
        javax.swing.JButton btnDialogue = new javax.swing.JButton(); 
        btnDialogue.setText("Message");
        javax.swing.JOptionPane.showMessageDialog(this, "Record Taken!");
        

        }
        catch(HeadlessException | SQLException Ex)
        {
            javax.swing.JOptionPane.showMessageDialog(null,Ex);
        }
        finally
        {
            try
            {
                stStatement.close();
            }
             catch(SQLException Ex)
            {
            javax.swing.JOptionPane.showMessageDialog(null,"Connection String not Closed" + " " + Ex);
            }
            
        }
        
        
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
      
        setUptextFields();
        getDataAndDisplay();
        String strDBConnectionString = "jdbc:mysql://localhost:3306/gate_control";
        String strDBUser="root";
        String strDBPassword="NewPassword";
        java.sql.Connection conMySQLConnectionString;

        try 
        {
           
            conMySQLConnectionString = DriverManager.getConnection(strDBConnectionString,strDBUser,strDBPassword);
        
            
        String strQuery = "UPDATE security_guards SET Name=?, Last_Name=?, Shift=?, Contact_Number=? where App_Password=" +txtAppPassword.getText();
         
        PreparedStatement s = conMySQLConnectionString.prepareStatement(strQuery);
        s.setString(1, txtName.getText());
        s.setString(2, txtLastName.getText());
        s.setString(3, txtShift.getText());
        s.setString(4, txtContactNumber.getText());
        s.executeUpdate(strQuery);
      
        javax.swing.JButton btnDialogue = new javax.swing.JButton(); 
        btnDialogue.setText("Message");
        javax.swing.JOptionPane.showMessageDialog(this, "Record Updated!");

        }
        catch(HeadlessException | SQLException Ex)
        {
            javax.swing.JOptionPane.showMessageDialog(null,Ex);
        }
      
            
    }//GEN-LAST:event_btnEditActionPerformed

