        public void getDataAndDisplay(){
            
        setUptextFields();
        
        if((name.isEmpty()) || (lastname.isEmpty()) || (shift.isEmpty()) || (contactnumber.isEmpty()) || 
                (apppassword.isEmpty()) ){
            JOptionPane.showMessageDialog(null,"Error: Missing values");
        
        }
        else{
            DefaultTableModel model = (DefaultTableModel)jSecurityOfficersTable.getModel();
            model.addRow(new Object[]{txtName.getText(),txtLastName.getText(),txtShift.getText(),
                txtContactNumber.getText(),txtAppPassword.getText(),});
        } //end else
        
        }//end getDataAndDisplay method
    
